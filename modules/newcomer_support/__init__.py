# Newcomer Growth Support Module
# This program is designed to boost non-working income for new members joining the Matrix system
# Key Benefits:
# 1. Instant Bonus: Upon joining Matrix, members receive an Instant Bonus from Newcomer Growth Support (NGS)
# 2. Extra Earning Opportunities (Monthly): At the end of the month (30 days), members gain extra earning opportunities
# 3. Upline Rank Bonus (10%): When a member achieves the same rank as their Upline, they receive an additional 10% Bonus
# All these benefits are combined into one program to ensure an immediate and sustained earning journey
